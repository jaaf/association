Cette marche a lieu tous les mardis matin à partir de neuf heures. Elle se termine en général avant midi. Il ne s’agit pas d’une marche 
sportive mais plutôt d’une promenade permettant de faire connaître les environs immédiats d’Amendeuix-Oneix.
 Le point de départ est le parking en face de la salle Denendako à la mairie d’Amendeuix-Oneix. Contact : Marie-Louise Marcarie (se rendre sur la page « Contacts » 
 pour les coordonnées). À voir aussi, le blog sur la marche du mardi tenu par un adhérent.

