        <h1 class="text-2xl text-blue-800">Introduction</h1>
        <p>Avant de téléverser des images sur le site vous devez créer le dossier qui va les acceuillir.</p>
        <p>Si c'est la première fois que vous le faite, vous êtes d'emblée placé dans un dossier qui vous est propre intitulé
             <span class=" text-red-600">« User-xx »</span> où xx est votre numéro d'utilisateur.</p>

        <h1 class="text-2xl text-blue-800">Créer des sous-dossiers</h1>
        <p>Mettre toutes ses photos dans le même sac complique fortement la recherche. C'est pourquoi je vous recommande de créer des sous-dossiers dans votre dossier racine.</p>
        <p>Donnez leur des noms simples mais évocateurs. Par exemple pour les photos d'un voyage, 
            vous pourriez créer un sous-dossier <span class=" text-red-600">« Voyage 2020 »</span>, 
             ou alors un sous-dossier <span class=" text-red-600">« Voyages »</span> dans lequel vous pourriez créer un dossier par année.</p>
        <p>Vous pouvez créer un sous-dossier en cliquant sur le bouton <span class=" text-red-600">« Nouveau dossier »</span> en fin du fil d'ariane.</p>
        <p>Le fil d'ariane est la suite de noms de dossier séparés par des flèches triangulaires que vous voyez juste sous la petite barre rose.</p>
        <p>Une fois que vous avez cliqué sur <span class=" text-red-600">« Nouveau dossier »</span>  une zone de saisie apparait qui vous permet de donner
             un nom au dossier que vous désirez créer.</p>
         <p>Cliquez ensuite sur <span class=" text-red-600">« Créer le dossier »</span> pour le créer. Il doit tout de suite apparaitre dans la liste des 
             dossiers en dessous du fil d'ariane.</p>  

        <h1 class="text-2xl text-blue-800">Se placer dans un dossier</h1>
        <p>Pour se placer (c.-à-d. l'ouvrir), il suffit de cliquer sur son nom, soit dans le fil d'ariane 
            (pour remonter l'arborescence), soit dans la liste des dossiers (pour descendre dans l'arborescence)</p>
            <p>Faites cependant attention de ne pas cliquer sur le bouton rouge avec un croix. Ce dernier provoque la suppression.</p>
        
         <h1 class="text-2xl text-blue-800">Supprimer un fichier ou un dossier</h1>
         <p>Pour supprimer un ficher ou un dossier, cliquez sur le bouton rouge marqué d'une croix qui l'accompagne.</p>

         <h1 class="text-2xl text-blue-800">Téléverser des images</h1>
         <p >Je vous recommande une taille d'image inférieure à 2 Mo de préférence toutes en format paysage. </p>
             <p class=" text-red-700"> Sachez que le logiciel refuse les images d'une taille supérieure à 5 Mo.</p>
         <p>Pour téléverser des images, cliquez sur le bouton <span class=" text-red-600">« Select Fichiers »</span>. Un explorateur s'ouvre et vous permet de sélectionner un ou plusieur fichier.</p>
         <p>Ensuite cliquez sur « Sauvegarder les images ». Les fichiers, une fois téléversés, apparaitront dans la liste.</p>
